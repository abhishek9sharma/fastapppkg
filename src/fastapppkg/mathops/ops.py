def addop(a, b):
    """Returns the sum of two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of the two numbers.
    """
    return a + b


def subop(a, b):
    """Subtracts two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The result of subtracting b from a.
    """
    return a - b


def multop(a, b):
    """Multiply two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The result of multiplying the two numbers.
    """
    return a * b


def invertop(a):
    """Return the inverse of the input number.

    Args:
        a: The input number for which inverse needs to be calculated.

    Returns:
        The inverse of the input number as a negative value.
    """
    return -1
